/**
* op_add - function adds two ints
* @a: int one
* @b: int two
* Return: returns sum of a and b
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - function subtracts two ints
* @a: int one
* @b: int two
* Return: returns the difference of a and b
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - function multiplies two ints
* @a: int one
* @b: int two
* Return: returns the product of a and b
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - function divides two ints
* @a: int one
* @b: int two
* Return: returns the result of the division of a by b
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - function gets the remainder of the division of two ints
* @a: int one
* @b: int two
* Return: returns the remainder of the division of a by b
*/
int op_mod(int a, int b)
{
	return (a % b);
}
